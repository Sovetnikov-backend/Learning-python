"""
–í–ï–†–°–ò–Ø 1.3: –°–æ—Ö—Ä–∞–Ω—è–µ–º –¥–µ—Ç–∞–ª–∏–∑–∞—Ü–∏—é –∏–∑ v1.2 + –¥–æ–±–∞–≤–ª—è–µ–º –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏–π –∞–Ω–∞–ª–∏–∑
"""

print("="*50)
print("–°–ò–°–¢–ï–ú–ê –ö–û–ù–¢–†–û–õ–Ø –ü–†–û–ò–ó–í–û–î–°–¢–í–ê v1.3")
print("–î–µ—Ç–∞–ª–∏–∑–∞—Ü–∏—è –æ—à–∏–±–æ–∫ + –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏–π –∞–Ω–∞–ª–∏–∑")
print("="*50)

# 1. –í–í–û–î –î–ê–ù–ù–´–• –° FLOAT
temperature = float(input("–¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ (¬∞C): "))
pressure = float(input("–î–∞–≤–ª–µ–Ω–∏–µ (–±–∞—Ä): "))
raw_material = float(input("–°—ã—Ä—å—ë (–∫–≥): "))

print("\n" + "="*50)
print("–î–ï–¢–ê–õ–ò–ó–ò–†–û–í–ê–ù–ù–´–ô –ê–ù–ê–õ–ò–ó (—Å–æ—Ö—Ä–∞–Ω—è–µ–º –∏–∑ v1.2):")
print("="*50)

# –°—á—ë—Ç—á–∏–∫ –ø—Ä–æ–±–ª–µ–º
problems_count = 0

# 1. –ü–†–û–í–ï–†–ö–ê –¢–ï–ú–ü–ï–†–ê–¢–£–†–´ –° –î–ï–¢–ê–õ–ò–ó–ê–¶–ò–ï–ô
if 18 <= temperature <= 25:
    print(f"‚úÖ –¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞: {temperature}¬∞C (–Ω–æ—Ä–º–∞: 18-25¬∞C)")
else:
    problems_count += 1
    print(f"‚ùå –¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞: {temperature}¬∞C")

    # –í–ª–æ–∂–µ–Ω–Ω–æ–µ —É—Å–ª–æ–≤–∏–µ –¥–ª—è –¥–µ—Ç–∞–ª–∏–∑–∞—Ü–∏–∏
    if temperature < 18:
        difference = 18 - temperature
        print(f"   ‚ö†Ô∏è  –°–ª–∏—à–∫–æ–º –Ω–∏–∑–∫–∞—è! –ù—É–∂–Ω–æ –Ω–∞–≥—Ä–µ—Ç—å –Ω–∞ {difference:.1f}¬∞C")
    else:  # –∑–Ω–∞—á–∏—Ç temperature > 25
        difference = temperature - 25
        print(f"   ‚ö†Ô∏è  –°–ª–∏—à–∫–æ–º –≤—ã—Å–æ–∫–∞—è! –ù—É–∂–Ω–æ –æ—Ö–ª–∞–¥–∏—Ç—å –Ω–∞ {difference:.1f}¬∞C")

# 2. –ü–†–û–í–ï–†–ö–ê –î–ê–í–õ–ï–ù–ò–Ø –° –î–ï–¢–ê–õ–ò–ó–ê–¶–ò–ï–ô
if 100 <= pressure <= 200:
    print(f"‚úÖ –î–∞–≤–ª–µ–Ω–∏–µ: {pressure} –±–∞—Ä (–Ω–æ—Ä–º–∞: 100-200 –±–∞—Ä)")
else:
    problems_count += 1
    print(f"‚ùå –î–∞–≤–ª–µ–Ω–∏–µ: {pressure} –±–∞—Ä")

    # –í–ª–æ–∂–µ–Ω–Ω–æ–µ —É—Å–ª–æ–≤–∏–µ
    if pressure < 100:
        difference = 100 - pressure
        print(f"   ‚ö†Ô∏è  –°–ª–∏—à–∫–æ–º –Ω–∏–∑–∫–æ–µ! –ù—É–∂–Ω–æ –ø–æ–≤—ã—Å–∏—Ç—å –Ω–∞ {difference:.1f} –±–∞—Ä")
    else:  # pressure > 200
        difference = pressure - 200
        print(f"   ‚ö†Ô∏è  –°–ª–∏—à–∫–æ–º –≤—ã—Å–æ–∫–æ–µ! –ù—É–∂–Ω–æ —Å–Ω–∏–∑–∏—Ç—å –Ω–∞ {difference:.1f} –±–∞—Ä")

# 3. –ü–†–û–í–ï–†–ö–ê –°–´–†–¨–Ø
if raw_material > 500:
    print(f"‚úÖ –°—ã—Ä—å—ë: {raw_material} –∫–≥ (–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ, –Ω—É–∂–Ω–æ >500 –∫–≥)")
else:
    problems_count += 1
    print(f"‚ùå –°—ã—Ä—å—ë: {raw_material} –∫–≥")
    difference = 500 - raw_material
    print(f"   ‚ö†Ô∏è  –ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ! –ù—É–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å {difference:.1f} –∫–≥")

print("\n" + "="*50)
print("–ú–ê–¢–ï–ú–ê–¢–ò–ß–ï–°–ö–ò–ô –ê–ù–ê–õ–ò–ó (–ù–û–í–û–ï –≤ v1.3):")
print("="*50)

# –ò–°–ü–û–õ–¨–ó–£–ï–ú –ò–ó–£–ß–ï–ù–ù–´–ï –§–£–ù–ö–¶–ò–ò:

# 1. ABS() - –ú–û–î–£–õ–¨ –û–¢–ö–õ–û–ù–ï–ù–ò–ô
temperature_deviation = abs(temperature - 22)  # –æ—Ç –∏–¥–µ–∞–ª–∞ 22¬∞C
pressure_deviation = abs(pressure - 150)       # –æ—Ç –∏–¥–µ–∞–ª–∞ 150 –±–∞—Ä
raw_material_shortage = max(0, 500 - raw_material)  # –µ—Å–ª–∏ —Å—ã—Ä—å—è –º–∞–ª–æ

print(f"\nüìê –†–ê–°–ß–Å–¢ –û–¢–ö–õ–û–ù–ï–ù–ò–ô (abs()):")
print(f"   –¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ –æ—Ç–∫–ª–æ–Ω—è–µ—Ç—Å—è –Ω–∞: {temperature_deviation:.1f}¬∞C")
print(f"   –î–∞–≤–ª–µ–Ω–∏–µ –æ—Ç–∫–ª–æ–Ω—è–µ—Ç—Å—è –Ω–∞: {pressure_deviation:.1f} –±–∞—Ä")
if raw_material_shortage > 0:
    print(f"   –ù–µ—Ö–≤–∞—Ç–∫–∞ —Å—ã—Ä—å—è: {raw_material_shortage:.1f} –∫–≥")

# 2. MAX() - –ù–ê–•–û–î–ò–ú –°–ê–ú–£–Æ –ë–û–õ–¨–®–£–Æ –ü–†–û–ë–õ–ï–ú–£
all_deviations = [temperature_deviation, pressure_deviation, raw_material_shortage]
max_deviation = max(all_deviations)

print(f"\nüéØ –°–ê–ú–ê–Ø –ë–û–õ–¨–®–ê–Ø –ü–†–û–ë–õ–ï–ú–ê (max()):")
print(f"   –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ: {max_deviation:.1f}")

# –û–ø—Ä–µ–¥–µ–ª—è–µ–º, –∫–∞–∫–æ–π –ø–∞—Ä–∞–º–µ—Ç—Ä –∏–º–µ–µ—Ç –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ
if max_deviation == temperature_deviation:
    print("   –ù–∞–∏–±–æ–ª–µ–µ –∫—Ä–∏—Ç–∏—á–Ω—ã–π –ø–∞—Ä–∞–º–µ—Ç—Ä: –¢–ï–ú–ü–ï–†–ê–¢–£–†–ê")
elif max_deviation == pressure_deviation:
    print("   –ù–∞–∏–±–æ–ª–µ–µ –∫—Ä–∏—Ç–∏—á–Ω—ã–π –ø–∞—Ä–∞–º–µ—Ç—Ä: –î–ê–í–õ–ï–ù–ò–ï")
else:
    print("   –ù–∞–∏–±–æ–ª–µ–µ –∫—Ä–∏—Ç–∏—á–Ω—ã–π –ø–∞—Ä–∞–º–µ—Ç—Ä: –°–´–†–¨–Å")

# 3. MIN() - –ù–ê–•–û–î–ò–ú –ù–ê–ò–ú–ï–ù–¨–®–ï–ï –û–¢–ö–õ–û–ù–ï–ù–ò–ï
min_deviation = min(all_deviations)
print(f"\n‚úÖ –ù–ê–ò–ú–ï–ù–¨–®–ê–Ø –ü–†–û–ë–õ–ï–ú–ê (min()):")
print(f"   –ú–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ: {min_deviation:.1f}")

# 4. ROUND() - –û–ö–†–£–ì–õ–Ø–ï–ú –î–õ–Ø –ß–ò–¢–ê–ï–ú–û–°–¢–ò
print(f"\nüìä –û–ö–†–£–ì–õ–Å–ù–ù–´–ï –ó–ù–ê–ß–ï–ù–ò–Ø (round()):")
print(f"   –¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞: {round(temperature, 1)}¬∞C")
print(f"   –î–∞–≤–ª–µ–Ω–∏–µ: {round(pressure, 1)} –±–∞—Ä")
print(f"   –°—ã—Ä—å—ë: {round(raw_material, 1)} –∫–≥")

print("\n" + "="*50)
print("–ò–¢–û–ì–û–í–û–ï –†–ï–®–ï–ù–ò–ï")
print("="*50)

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –≤—Å–µ —É—Å–ª–æ–≤–∏—è —Å –ø–æ–º–æ—â—å—é AND (–∏–∑—É—á–µ–Ω–Ω–∞—è —Ç–µ–º–∞)
all_conditions_ok = (
    18 <= temperature <= 25 and 
    100 <= pressure <= 200 and 
    raw_material > 500
)

if all_conditions_ok:
    print("üéâ –í–°–ï –ü–ê–†–ê–ú–ï–¢–†–´ –í –ù–û–†–ú–ï!")
    print("–°–ú–ï–ù–ê –ú–û–ñ–ï–¢ –ù–ê–ß–ò–ù–ê–¢–¨–°–Ø ‚úÖ")
else:
    print("üö´ –ï–°–¢–¨ –ù–ê–†–£–®–ï–ù–ò–Ø –ü–ê–†–ê–ú–ï–¢–†–û–í")
    print("–°–ú–ï–ù–£ –ù–ê–ß–ò–ù–ê–¢–¨ –ù–ï–õ–¨–ó–Ø ‚ùå")

# –ò—Å–ø–æ–ª—å–∑—É–µ–º –≤–ª–æ–∂–µ–Ω–Ω—ã–µ —É—Å–ª–æ–≤–∏—è –¥–ª—è –¥–µ—Ç–∞–ª–∏–∑–∞—Ü–∏–∏ –∏—Ç–æ–≥–∞
if problems_count == 0:
    print(f"\n–û–±–Ω–∞—Ä—É–∂–µ–Ω–æ {problems_count} –ø—Ä–æ–±–ª–µ–º. –û—Ç–ª–∏—á–Ω–∞—è —Ä–∞–±–æ—Ç–∞!")
elif problems_count == 1:
    print(f"\n–û–±–Ω–∞—Ä—É–∂–µ–Ω–∞ {problems_count} –ø—Ä–æ–±–ª–µ–º–∞.")
    print("–ù–µ–æ–±—Ö–æ–¥–∏–º–æ —É—Å—Ç—Ä–∞–Ω–∏—Ç—å –µ—ë –ø–µ—Ä–µ–¥ –Ω–∞—á–∞–ª–æ–º —Å–º–µ–Ω—ã.")
elif problems_count == 2:
    print(f"\n–û–±–Ω–∞—Ä—É–∂–µ–Ω—ã {problems_count} –ø—Ä–æ–±–ª–µ–º—ã.")
    print("–ù–µ–æ–±—Ö–æ–¥–∏–º–æ —É—Å—Ç—Ä–∞–Ω–∏—Ç—å –æ–±–µ –ø—Ä–æ–±–ª–µ–º—ã.")
else:  # problems_count == 3
    print(f"\n–û–±–Ω–∞—Ä—É–∂–µ–Ω—ã {problems_count} –ø—Ä–æ–±–ª–µ–º—ã.")
    print("–ù–µ–æ–±—Ö–æ–¥–∏–º–æ —É—Å—Ç—Ä–∞–Ω–∏—Ç—å –≤—Å–µ –ø—Ä–æ–±–ª–µ–º—ã.")

# –î–û–ü–û–õ–ù–ò–¢–ï–õ–¨–ù–û –í v1.3: –ú–ê–¢–ï–ú–ê–¢–ò–ß–ï–°–ö–ò–ô –ò–¢–û–ì
print(f"\nüìà –ú–ê–¢–ï–ú–ê–¢–ò–ß–ï–°–ö–ò–ô –ò–¢–û–ì:")
print(f"   –°—Ä–µ–¥–Ω–µ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ: {round(sum(all_deviations)/3, 2)}")
print(f"   –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ: {round(max_deviation, 2)}")
